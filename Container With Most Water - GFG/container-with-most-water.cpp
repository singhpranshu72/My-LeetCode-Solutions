long long maxArea(long long arr[], int n)
{  long long l = 0, r = n-1, ans = 0;
    while (l < r){
        ans = max(ans, (r - l) * min(arr[l], arr[r]));
        min(arr[l], arr[r]) == arr[l]? l++: r--;
    }
    return ans;
}

